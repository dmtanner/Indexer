package shared.model;

/** holds a value*/
public class Value {
	
	/** the id of this value*/
	private int id;
	/** the string input that the user entered for this value*/
	private String string;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getString() {
		return string;
	}
	public void setString(String string) {
		this.string = string;
	}
	
	
}
